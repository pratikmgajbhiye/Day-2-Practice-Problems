package com.BridgeLabz.Day2PracticeProblem;

public class SumofNatureNumberUsingForLoop {
	
	public static void main(String[] args)   
	{  
	int i, num = 5, sum = 0;  
	  
	for(i = 1; i <= num; ++i)  
	{  
	  
	sum = sum + i;  
	}  
	 
	System.out.println("Sum of Natural Numbers is = " + sum);  
	}

}
